'''
16. Дано ціле число, що позначає код символу по таблиці ASCII. Визначити, це код англійської
літери або будь-якої іншої символ.
'''


def check(num):
    if (65 <= num <= 90) or (97 <= num <= 122):
        print("Код позначає англійську літеру")
    else:
        print("Код не позначає англійську літеру")


a = int(input('Введіть код ASCII'))
check(a)
